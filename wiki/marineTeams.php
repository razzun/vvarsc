				<h3 style="padding-left: 4px; margin-left: 0">Squad and Fire-Team Compositions</h3>
				Note: The order of Squad Members listed below is not always directly related to their "Stack-Order" (the prescribed sequence of members for room-entry or other tasks such as Insertion/Extraction from COMTRANS vessels). In all cases except for the Full 8-Man Squad, the Squad Leader is played by a designated <strong>Squad Leader</strong> of the Platoon (at least E-5), and his Assistant Squad Leader (ASL) is normally the last man in the team and is responsible for team cohesion and confirming everyone is where they need to be. The ASL is usually the last man in the Stack and confirms their objectives are "clear" before moving onto the next. In the case of the Full 8-Man Squad, the Squad will be lead by either the <strong>Platoon Leader</strong>, or <strong>Assistant Platoon Leader</strong>, with a designated <strong>Squad Leader</strong> of the Platoon serving as the Assistant, and leader of the 2nd Fire-Team.
				<br />
				<br />
				Squad Sizes will be determined by the <strong>Platoon Leader</strong> upon formation of the OpUnit during the operation's planning phase. Many factors will influence the decided-upon Squad Size, including the strength of enemy forces, type of available Combat Transport assets, size of the Landing-Zone (LZ), required small-arms equipment, and more. Regardless, the Squad will always be comprised of at-least 4 members, and will always contain a dedicated <strong>Squad Leader</strong>, a <strong>Rifleman (Point-Man)</strong>, a <strong>Medic</strong>, and a second Rifleman serving as a Marksman, Heavy Weapons Operator, or Breacher / Demolitions specialist.
				<br />
				<br />
				As shown below, depending on the Squad size, individual members may be required to play multiple roles, such as a Medic performing the Duties of a <strong>Radio Operator</strong> in a 4-man Squad, or as the second Rifleman performing the role of <strong>Assistant Squad Leader</strong> in most cases besides the Full 8-Man Squad.
				
