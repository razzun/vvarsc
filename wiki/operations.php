				<h3 style="padding-left: 4px; margin-left: 0">Operational Units</h3>
				When an Organized Operation is being planned, Officers will evaluate the required assets (both physical and human) needed, and organize the participating players into Operational Units (OpUnit), each with their own focus during the mission.
				
				<h4 class="WikiH4">Squads, Flights, Elements, and Fire-Teams</h4>
				<h5 class="WikiH5">Overview</h5>
				<div class="WikiText">
					At the lowest-level, these OpUnits are built as Flights/Squads by the Squadron/Platoon to which they belong. These OpUnits are not persistent units as their parent-units are. As an example, a Flight-Leader of a Fighter Squadron may find him/herself leading 1st Flight one day, and 2nd Flight the next. For air units, a Flight normally consists of at least 2 ships, and the number of ships per Flight will vary based on the type of equipment operated by each Squadron during a particular operation. In some cases, especially with larger multi-crew ships, a single vessel may comprise the entire Flight. At this time, for ground units, a Squad consists of no-more than 8 members (including the Squad Leader).
					<br />
					<br />
					Flights and Squads may be further divided in-half to form <strong>Elements</strong> or <strong>Fire-Teams</strong>. In order for Elements/Fire-Teams to form, the Flight/Squad is required to have at least 4 distinct assets/personnel, so that when divided, each sub-unit has at least 2 assets/personnel. This rule is established to protect the SOP of most Military units which dictates that  wingmen never separate, and infantry operate in teams of two at-minimum.
				</div>
				<h5 class="WikiH5">Numbering and Naming</h5>
				<div class="WikiText">
					Because Flights and Squads are built from the Squadrons/Platoons to which they belong, they will always inherit the callsign of their parent-unit during an operation. In addition, the Flight/Squad will have a number corresponding to its order of creation during the operational planning phase.
					<br />
					<br />
					For an air unit, using an example of the 131st Recon Squadron, its Flights would be called: "Dagger 1", "Dagger 2", etc. The individual vessels within each Flight would be numbered "Dagger 1-1", "Dagger 1-2", and so on. For a ground unit, because the Platoon already inherits the callsign of its parent Special Warfare Team, the Squads of 1st Platoon (MSWT 1) would be called: "Thunder 1-1", "Thunder 1-2", etc. Normally, individual squad members of a ground unit wouldn't have their own callsign known to other units.
					<br />
					<br />
					When Flights and Squads are split into Elements or Fire-Teams, each Element is in-turn given a name. Using our example for air units, these would be named: "Dagger 1-Alpha", "Dagger 1-Bravo", etc. Each element would be formed using the first two vessels within the Flight, then the next two, and would continue on if the flight contains more than 4 vessels. In this system, a Flight-Leader can easily split his/her forces into smaller components if there is a need for detachment. For ground units, the same concept applies: "Alpha", "Bravo", etc is simply added to the Squad's callsign for each fire-team, resulting in "Thunder 1-1-Alpha", "Thunder 1-1-Bravo". Because we can rely on an Element never splitting any further, it can be addressed by a single callsign, rather than addressing each vessel or individual directly in sequence.
				</div>
				<br />
				
				<h4 class="WikiH4">Packages</h4>
				<h5 class="WikiH5">Overview</h5>
				<div class="WikiText">
					When multiple Flights/Squads operate together, they are known as a <strong>Package</strong>. There are no numerical limits to the size of a Package, but Officers will attempt to maintain ease of control for Package-leaders during operations by limiting their size as-needed after evaluating multiple factors. Based on this definition, a Package is usually tasked with performing a single objective as a group. Examples of this include, but are not limited to:
					<ul>
						<li>A Flight of Fighters escorting a single (or Flight of) large Military, Economic, or Command Asset(s)</li>
						<li>A combination of Fighter and Bomber Flights tasked with a coordinated attack on a single objective</li>
						<li>An Ground-Attack Flight and the Marine Unit to which it's attached for Close-Air-Support</li>
						<li>A Combat Transport Flight and the Marine Unit to which it's attached for support</li>
					</ul>
				</div>
				<h5 class="WikiH5">Numbering and Naming</h5>
				<div class="WikiText">
					Packages built for a given operation will be known as "Package Alpha", "Package Bravo", "Package Charlie", and so forth. Each Package's Command may also be given a distinct radio callsign. These callsigns will be unrelated to the Package's component units, and can be re-used between operations.
				</div>
				<br />
				
				<h4 class="WikiH4">Task-Force</h4>
				<h5 class="WikiH5">Overview</h5>
				<div class="WikiText">
					When an operation requires the participation of multiple Packages, each tasked with the same objective, or a related series of objectives, it is formed into a coordinated <strong>Task-Force</strong>. Due to the high level of planning required for operations of this size, a Task-Force is commanded by an Officer of O-5 PayGrade or higher. In some cases, a Task-Force may be lead by Flag Officers including Fleet Command Admirals.
				</div>
				<h5 class="WikiH5">Numbering and Naming</h5>
				<div class="WikiText">
					A Task-Force is given a number when it is formed, and that number is valid through the life of the operation, regardless of how many instances of the operation (missions) are performed. For example, if we have a weekly multi-section trade convoy with attached escort and reconnaissance units running from the Leir System to the Stanton System, these units would likely use the same Operational Plan (OpPlan) each time the mission is performed, and refine the OpPlan as needed. Therefore, if the Task-Force performing this operation is named "Task-Force 5", it will always be that case unless the OpPlan is retired and the mission no-longer performed. This is in-place so that Fleet and Division Command can can define a permanent structure for the Packages of that Task-Force needed to perform each operation, and reduce time in planning and organization of repeating operations.
				</div>
