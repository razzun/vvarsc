				<h4 class="WikiH4">Unit Types</h4>
				<h5 class="WikiH5">Squadrons & Platoons</h5>
				<div class="WikiText">
					Our Naval Commands (NAVCOMs) are built using a unit-hierarchy system. The lowest-level of the tree, and smallest permanent units are <strong>Squadrons</strong> and <strong>Platoons</strong>. These units are commanded by Junior Officers (O-1, O-2, O-3). At times, especially when an Officer is promoted and assigned to a higher command, authority to lead a Squadron or Platoon will be delegated to a Senior NCO of that unit. Each Squadron or Platoon will sortie one or more <strong>Flights</strong> or <strong>Squads</strong> for organized operations. These Flights/Squads are led by NCOs, but are not considered Permanent Units within the Fleet.
					<br />
					<br />
					For air units, a Flight normally consists of at least 2 ships, and the number of ships per Flight will vary based on the type of equipment operated by each Squadron during a particular operation. In some cases, especially with larger multi-crew ships, a single vessel may comprise the entire Flight. Most of the Fleet's Air Squadrons are built to support 4 concurrently operating Flights - this normally equates to a Squadron size of 16 members.
					<br />
					<br />
					For primary ground units, a Platoon consists of 20 Members. Personnel include a Commanding Officer, a Senior-Enlisted (at least E-6) serving as the assistant Platoon Leader, and three 6-man Squads of Marine Operators, each lead by an E-5 or higher.
					<br />
					<br />
					Once a Squadron or Platoon has reached its Player Capacity, a sister-unit can be formed alongside it. This will generally be accomplished by offering an Officer Commission to the Assistant Squadron/Platoon Leader of the existing unit, and promoting another NCO (likely a Flight/Squad Leader into the role of Assistant for the existing Unit). The new leader will enter Officer Training, and will serve as an apprentice and Assistant CO in his/her current unit before being placed in-command of the newly-created unit.
				</div>
				<h5 class="WikiH5">Air Wings & Marine Combat Teams</h5>
				<div class="WikiText">
					Moving up the hierarchy tree, we find the <strong>Air Wing</strong> and <strong>Marine Combat Team</strong> units. These units will only be activated once there are an adequate number of established Squadrons/Platoons existing below them, and there is a need for a parent-unit to be created for oversight in planning and operational execution. Due to constraints inherent to the numbering system described below, a single Air Wing can contain a maximum of 9 Squadrons. The Air Wing and Combat Team units will be commanded by an Officer of at-least O-4 PayGrade. Their command staff may also include additional Officers, and Senior NCOs serving as advisers or specialists.
				</div>
				<h5 class="WikiH5">Naval Commands</h5>
				<div class="WikiText">
					Finally, we reach the level of the Naval Command Headquarters (NAVCOM HQ). These units serve as the single, static container for all units of a certain combat discipline (Air Warfare, Surface Warfare (Marine), Logistics, and Special Warfare). The list of Naval Commands is also static, meaning there will never be multiple commands of the same type within the Fleet. This ensures that regardless of how large the Fleet grows, there is a single point-of-control for all units of a certain discipline directly beneath the Chief of Naval Operations at FLEETCOM. Naval Commands are led by a Senior Officer of at-least O-6 PayGrade, assisted by another Senior Officer of at-least O-5 PayGrade. Futhermore, additional Officers and Senior NCOs can be assigned to this level as advisers or specialized operatives performing unconventional roles for their Command.
				</div>
				<br />
				<h4 class="WikiH4">Unit Numbering and Naming</h4>
				<div class="WikiText">
					Larger-size units such as Air Wings/Combat Teams are numbered in the order they are created. Air Wing numbers are unique across the entire Fleet, as they are used both within Naval Air Forces Command (NAVAIRFORCOM) and Naval Special Warfare Command (NAVSPECWARCOM). Air Wings are not numbered by type - examples of this are "Fighter Wing 1", "Strike Wing 2", etc. There will be no reuse of these numbers within the Fleet. If a new Air Wing needs to be created, it will take-on the lowest available number after all other established Air Wings, regardless of its purpose.
					<br />
					<br />
					For Marine Combat Teams, these are simply numbered in the order of creation, based on their type. The examples would be "Infantry Combat Team 1 (ICT 1)","ICT 2", "Armored Combat Team 1 (ACT 1)", etc.
					<br />
					<br />
					All Squadrons within the Fleet are designated with a three-digit number, indicative of the Air Wing to which they belong. Squadron numbering starts at 110, with numbers 110 through 119 reserved for units of Fighter Wing 1, and Strike Wing 2 having Squadron numbers 120 through 129. This pattern continues for all Air Wings within the Fleet, regardless of the NAVCOM to which they belong. The 2nd digit of the Squadron number will always match the Air Wing's number.
					<br />
					<br />
					In addition, Squadrons are given a multi-letter designation. The complete alphanumerical designation of each Squadron is a combination of their unique 3-digit number, and letters signifying their roles, responsibilities, and capabilities. All Squadron Designations start with the letter “V”. This is a remnant of historical Naval Air Forces using this letter for all fixed-wing aircraft Squadrons, as opposed to rotary-wing aircraft. The subsequent letters in the designation are defined below - this list may be expanded as needs change.
					<ul class="WikiOL">
						<li><strong>A:</strong> Attack / Bomber</li>
						<li><strong>C:</strong> Combat Transport</li>
						<li><strong>E:</strong> Engineering & Repair</li>
						<li><strong>F:</strong> Fighter</li>
						<li><strong>L:</strong> Logistics Transport</li>
						<li><strong>M:</strong> Medical / Rescue</li>
						<li><strong>O:</strong> Fueling</li>
						<li><strong>Q:</strong> Electronic Warfare</li>
						<li><strong>R:</strong> Intelligence / Recon / Surveillance / Exploration & Survey</li>
						<li><strong>S:</strong> Science & Research</li>
						<li><strong>T:</strong> Training</li>
						<li><strong>U:</strong> Mining / Salvage</li>
						<li><strong>W:</strong> Airborne Early Warning / Command & Control</li>
						<li><strong>X:</strong> Experimental</li>
					</ul>
					<br />
					<br />
					For Marine Platoons, these simply are numbered in-order of creation within an Combat Team ("1st Platoon", "2nd Platoon", etc), where numbers will be reused between Combat Teams.
